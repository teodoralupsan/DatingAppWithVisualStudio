export function tokenGetter() {
   return localStorage.getItem('token');
}

@NgModule({
   declarations: [
      AppComponent,
      NavComponent,
      HomeComponent,
      RegisterComponent,
      ListsComponent,
      MemberListComponent,
      MessagesComponent,
      MemberCardComponent,
      MemberDetailComponent,
      MemberEditComponent
   ],
   imports: [
      BrowserModule,
      HttpClientModule,
      FormsModule,
      BrowserAnimationsModule,
      BsDropdownModule.forRoot(),
      TabsModule.forRoot(),
      CarouselModule.forRoot(),
      RouterModule.forRoot(appRoutes),
      JwtModule.forRoot({
         config: { // all the requests will have as header the token provided except for the blacklistedRoutes
            tokenGetter,
            whitelistedDomains: ['localhost:60671'],
            blacklistedRoutes: ['localhost:60671/api/auth'] // for this route the token is not sent
         }
      })
   ],
   providers: [
      AuthService,
      ErrorInterceptorProvider,
      MemberListResolver,
      MemberDetailResolver,
      MemberEditResolver
   ],
   bootstrap: [
      AppComponent
   ]
})
export class AppModule { }
